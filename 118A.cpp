#include <iostream>
#include <string>
#include <algorithm>

using namespace std;

int main()
{
    string s;
    cin >> s;

    // Convert string to lowercase
    transform(s.begin(), s.end(), s.begin(), ::tolower);

    // Remove vowels
    string result = "";
    for (int i = 0; i < s.length(); i++) {
        if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u' || s[i] == 'y') {
            continue;
        }
        result += '.';
        result += s[i];
    }

    cout << result << endl;

    return 0;
}


