#include <iostream>
using namespace std;

int main()
{
    long long int m, n, a, r1, r2;
    cin >> n >> m >> a;

    if (m % a == 0)
        r1 = m / a;
    else
        r1 = (m / a) + 1;

    if (n % a == 0)
        r2 = n / a;
    else
        r2 = (n / a) + 1;

    cout << r1 * r2 << endl;

    return 0;
}
