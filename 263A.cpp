#include <iostream>
#include <cmath>

using namespace std;

int main() {
    int r, c;
    for (int i = 1; i <= 5; i++) {
        for (int j = 1; j <= 5; j++) {
            int x;
            cin >> x;
            if (x == 1) {
                r = i;
                c = j;
            }
        }
    }
    int row_dist = abs(r - 3);
    int col_dist = abs(c - 3);
    int min_moves = row_dist + col_dist;
    cout << min_moves << endl;
    return 0;
}
