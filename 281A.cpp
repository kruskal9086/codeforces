#include <iostream>
#include <string>
#include <cctype>

using namespace std;

int main()
{
    string str;
    cout << "Enter a string: ";
    getline(cin, str);

    // Capitalize the first letter of each word
    for (int i = 0; i < str.length(); i++) {
        // Check if the current character is the first character of a word
        if (i == 0 || isspace(str[i - 1])) {
            str[i] = toupper(str[i]);
        }
    }

    cout << "Capitalized string: " << str << endl;

    return 0;
}




