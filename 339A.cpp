#include <iostream>
#include <string>
#include <algorithm>
using namespace std;

int main() {
    string sum;
    cin >> sum;

    // Count the frequency of each digit
    int count[4] = { 0, 0, 0, 0 };
    for (char c : sum) {
        count[c - '0']++;
    }

    // Construct the rearranged sum
    string rearranged_sum;
    for (int i = 1; i <= 3; i++) {
        for (int j = 0; j < count[i]; j++) {
            rearranged_sum += to_string(i);
            rearranged_sum += "+";
        }
    }
    // Remove the trailing '+' character
    rearranged_sum.pop_back();

    cout << rearranged_sum << endl;

    return 0;
}
